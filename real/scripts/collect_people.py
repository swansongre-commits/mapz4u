def main():
    people = []
    for tid in TOPICS:
        if tid not in P: continue
        name, era, verb, trial, lineage, anti = P[tid]
        people.append(dict(id=f"person-{tid}", name=name, era=era, topic_tags=[tid],
            verb_desc=verb, story_trial=trial, sites=[], job_lineage=lineage,
            anti_stereotype=anti))
    write_json(rp("data","raw","people.json"), people)
    anti = sum(p["anti_stereotype"] for p in people)
    print(f"인물 {len(people)}명 (반고정관념 {anti}명)")

if __name__ == "__main__":
    main()
